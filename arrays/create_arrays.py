cars = ["Ford", "Volvo", "BMW"]

print(cars)

# access array index value
x = cars[0]

print(x)

# change value of array index
cars[0] = "Toyota"

print(cars)

# array length
x = len(cars)

print(x)

# loop through array
for x in cars:
    print(x)

# add element to array
cars.append("Honda")

print(cars)

# remove an element from array
cars.pop(1)

print(cars)
