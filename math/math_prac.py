import math

# min and max function
x = min(5, 10, 25)
y = max(5, 10, 25)

print(x)
print(y)

# absolute value(positive number)
x = abs(-7.25)

print(x)

# power of function
x = pow(4, 3)

print(x)

# square root function
x = math.sqrt(64)

print(x)

# round a number upward to its nearest integer
x = math.ceil(1.4)

print(x)

# round a number downward to its nearest integer
x = math.floor(1.4)

print(x)

# return the value of pie
x = math.pi

print(x)
